<script>
import { mapState } from 'vuex'
import Swiper from 'swiper'
export default {
  name: 'ListContainer',
  mounted() {
    // mounted组件挂载完毕(组件的结构,DOM全有了)
    this.$store.dispatch('getBannerList')
    // new Swiper 之前必须有页面结构
    // 但是结构中有v-for 它是渲染服务器请求回来的数据(服务器返回了数据,才有渲染结构) 是异步的
    // 仓库刚收到数据 但是这边的mounte首先已经执行了new Swiper 但Swiper没有结构  所以不行
    // mounted -> 获取到服务器的数据 -> new Swiper -> 存入state
  },
  computed: {
    ...mapState({
      bannerList: state => state.home.bannerList,
    }),
  },
  watch: {
    // 监听 bannerList数据的变化,因为这条数据发生过变化 [] -> [四条数据]
    bannerList: {
      // 如果执行handler代表组件实例的身上这个属性的属性值已经有了(数组四条)
      // 当前的函数执行 只能保证数据已经有了 不能保证v-for渲染完了没有
      // v-for 执行完毕了才有结构  new Swiper 之前必须有页面结构
      handler() {
        // nextTick  在下次([] -> [四条数据])DOM更新循环结束(v-for)之后执行延迟回调
        // 在修改数据之后([] -> [四条数据])立即使用这个方法,获取更新后的 DOM(new Swiper)
        // v-for已经结束的之后在执行 new Swiper
        // 当执行这个回调式已经保证服务器的数据已经回来了
        this.$nextTick(() => {
          new Swiper(this.$refs.mySwiper, {
            loop: true, // 循环模式选项
            // 如果需要分页器
            pagination: {
              el: '.swiper-pagination',
              clickable: true,
            },
            // 如果需要前进后退按钮
            navigation: {
              nextEl: '.swiper-button-next',
              prevEl: '.swiper-button-prev',
            },
            autoplay: {
              delay: 1000, //1秒切换一次
            },
          })
        })
      },
    },
  },
}
</script>

